static dword
calc_security(KeyData *kd , byte *table1 , byte *table2)
{
    byte ltr, tab1, tab2;
    dword key = 0;

    for(word cnt = 0; cnt < 32; cnt++)  // encode each letter
    {
        tab1 = table1[cnt];             // take a byte from the 1st table
        tab2 = table2[cnt];             // take a byte from the 2nd table

        ltr = kd->sysopName[cnt] ^ kd->bbsName[cnt] ^ kd->createdBy[cnt] ^ tab1;

        key = key + dword(ltr * tab2 * (cnt ^ tab1) * (kd->beta ^ ltr));
    }

    return key;                        // return the key value
}

static char *cryptext;
static int   crypt_ptr;
static int   crypt_length;

inline void
crypt(char *buf)
{
   *buf ^= cryptext[crypt_ptr] ^ (cryptext[0] * crypt_ptr);

   cryptext[crypt_ptr] += ((crypt_ptr < (crypt_length - 1)) ?
                           cryptext[crypt_ptr + 1] : cryptext[0]);

   if(!cryptext[crypt_ptr]) cryptext[crypt_ptr] += 1;

   if(++crypt_ptr >= crypt_length) crypt_ptr = 0;
}

static void
encrypt(char *text,int len,char *key)
{
   char x[101];
   strcpy(x,key);

   crypt_length = strlen(key);
   crypt_ptr = 0;
   cryptext = x;

   for(int i=0;i<len;i++)
      crypt(text++);
}


